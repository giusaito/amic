     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $query = $request->query('keyword');
        if($query) {
            $category = CategoryLink::where('name', 'LIKE', "%$query%")->select('id', 'name')->get();
            return response()->json($category);
        } else {
            $category = CategoryLink::orderBy('id', 'DESC')->select('id', 'name')->get();
            return response()->json($category);
        }
    }

